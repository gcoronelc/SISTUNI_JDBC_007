
package ENTIDADES;

public class Empleador {
    String cod_emp;
    String nro_doc_emp;
    String nom_emp;
    String firm_emp;

    public Empleador() {
    }

    public String getCod_emp() {
        return cod_emp;
    }

    public void setCod_emp(String cod_emp) {
        this.cod_emp = cod_emp;
    }

    public String getNro_doc_emp() {
        return nro_doc_emp;
    }

    public void setNro_doc_emp(String nro_doc_emp) {
        this.nro_doc_emp = nro_doc_emp;
    }

    public String getNom_emp() {
        return nom_emp;
    }

    public void setNom_emp(String nom_emp) {
        this.nom_emp = nom_emp;
    }

    public String getFirm_emp() {
        return firm_emp;
    }

    public void setFirm_emp(String firm_emp) {
        this.firm_emp = firm_emp;
    }
    
}
