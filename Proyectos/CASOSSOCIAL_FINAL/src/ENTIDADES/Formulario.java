
package ENTIDADES;

public class Formulario {
    String nroform;
    String tipo_seg;
    int nro_folio;
    String cod_tit_form;
    String aut_correo;
    String firma_essalud;
    int nit_essalud;

    public Formulario() {
    }

    public String getNroform() {
        return nroform;
    }

    public void setNroform(String nroform) {
        this.nroform = nroform;
    }

    public String getTipo_seg() {
        return tipo_seg;
    }

    public void setTipo_seg(String tipo_seg) {
        this.tipo_seg = tipo_seg;
    }

    public int getNro_folio() {
        return nro_folio;
    }

    public void setNro_folio(int nro_folio) {
        this.nro_folio = nro_folio;
    }

    public String getCod_tit_form() {
        return cod_tit_form;
    }

    public void setCod_tit_form(String cod_tit_form) {
        this.cod_tit_form = cod_tit_form;
    }

    public String getAut_correo() {
        return aut_correo;
    }

    public void setAut_correo(String aut_correo) {
        this.aut_correo = aut_correo;
    }

    public String getFirma_essalud() {
        return firma_essalud;
    }

    public void setFirma_essalud(String firma_essalud) {
        this.firma_essalud = firma_essalud;
    }

    public int getNit_essalud() {
        return nit_essalud;
    }

    public void setNit_essalud(int nit_essalud) {
        this.nit_essalud = nit_essalud;
    }

}
