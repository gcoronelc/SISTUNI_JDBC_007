
package ENTIDADES;

public class Documento {
    String nro_doc;
    String tipo_doc;
    String nom_comp_doc;

    public Documento() {
    }

    public String getNro_doc() {
        return nro_doc;
    }

    public void setNro_doc(String nro_doc) {
        this.nro_doc = nro_doc;
    }

    public String getTipo_doc() {
        return tipo_doc;
    }

    public void setTipo_doc(String tipo_doc) {
        this.tipo_doc = tipo_doc;
    }

    public String getNom_comp_doc() {
        return nom_comp_doc;
    }

    public void setNom_comp_doc(String nom_comp_doc) {
        this.nom_comp_doc = nom_comp_doc;
    }
    
}
